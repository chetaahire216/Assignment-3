public class TableOfFive {
    public static void main(String[] args) {
        int number = 5; // Number to print the table of
        
        // Loop to print the multiplication table of 5
        for (int i = 1; i <= 10; i++) {
            System.out.println(number + " x " + i + " = " + (number * i));
        }
    }
}